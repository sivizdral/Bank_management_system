    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String lokacija = "http://localhost:8080/centralniserver/centralni/komitenti";
            String endpoint = uriPolje.getText().substring(48);
            
            URL url = new URL(uriPolje.getText());
            HttpURLConnection con = (HttpURLConnection) url.openConnection();
            
            int responseCode = 0;
            String inputString = null;
            
            String method = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
            if (!method.equals("PUT")) con.setRequestMethod(method);
            else con.setRequestMethod("POST");
            
            StringBuffer queryParam = new StringBuffer();
            JTextArea odgovor = null;
            
            if (method.equals("POST")) {
            
                switch(endpoint) {
                    case "komitenti":
                    {

                        if (komitentNaziv.getText().length() == 0 || komitentAdresa.getText().length() == 0 || komitentSediste.getText().length() == 0) {
                            komitentOdgovor.setText("Polja nisu ispravno uneta!");
                            return;
                        } else komitentOdgovor.setText("");

                        queryParam.append("naziv=");
                        queryParam.append(komitentNaziv.getText());
                        queryParam.append("&");
                        queryParam.append("adresa=");
                        queryParam.append(komitentAdresa.getText());
                        queryParam.append("&");
                        queryParam.append("sediste=");
                        queryParam.append(komitentSediste.getText());

                        odgovor = komitentOdgovor;

                    } break;
                    
                    case "mesta": {
                        
                        if (mestoNaziv.getText().length() == 0 || mestoPostanskiBroj.getText().length() == 0) {
                            mestoOdgovor.setText("Polja nisu ispravno uneta!");
                            return;
                        } else mestoOdgovor.setText("");

                        queryParam.append("naziv=");
                        queryParam.append(mestoNaziv.getText());
                        queryParam.append("&");
                        queryParam.append("postanskiBroj=");
                        queryParam.append(mestoPostanskiBroj.getText());

                        odgovor = mestoOdgovor;
                        
                    } break;
                    
                    case "filijale": {
                        
                        if (filijalaNaziv.getText().length() == 0 || filijalaAdresa.getText().length() == 0 || filijalaMesto.getText().length() == 0) {
                            filijalaOdgovor.setText("Polja nisu ispravno uneta!");
                            return;
                        } else filijalaOdgovor.setText("");

                        queryParam.append("naziv=");
                        queryParam.append(filijalaNaziv.getText());
                        queryParam.append("&");
                        queryParam.append("adresa=");
                        queryParam.append(filijalaAdresa.getText());
                        queryParam.append("&");
                        queryParam.append("mesto=");
                        queryParam.append(filijalaMesto.getText());

                        odgovor = filijalaOdgovor;

                    } break;
                    
                    case "racuni": {
                        
                        if (racunKomitent.getText().length() == 0 || racunDozvoljeniMinus.getText().length() == 0 || racunMesto.getText().length() == 0) {
                            racunOdgovor.setText("Polja nisu ispravno uneta!");
                            return;
                        } else racunOdgovor.setText("");

                        queryParam.append("komitent=");
                        queryParam.append(racunKomitent.getText());
                        queryParam.append("&");
                        queryParam.append("dozvoljeniMinus=");
                        queryParam.append(racunDozvoljeniMinus.getText());
                        queryParam.append("&");
                        queryParam.append("mesto=");
                        queryParam.append(racunMesto.getText());

                        odgovor = racunOdgovor;
                        
                    } break;
                    
                    case "transakcije": {
                        
                        String t = null;
                        for (Enumeration<AbstractButton> buttons = buttonGroup2.getElements(); buttons.hasMoreElements();) {
                            AbstractButton button = buttons.nextElement();

                            if (button.isSelected()) {
                                t = button.getText();
                            }
                        }
                        
                        String tip = null;
                        if (t == null) {
                            transakcijaOdgovor.setText("Morate odabrati tip transakcije!");
                            return;
                        }
                        if (t.equals("Uplata")) tip = "uplata";
                        else if (t.equals("Isplata")) tip = "isplata";
                        else if (t.equals("Prenos")) tip = "prenos";
                        
                        if (tip.equals("uplata") && transakcijaRacunKa.getText().length() == 0) {
                            transakcijaOdgovor.setText("Morate uneti racun na koji uplacujete novac!");
                            return;
                        }
                        
                        if (tip.equals("isplata") && transakcijaRacunSa.getText().length() == 0) {
                            transakcijaOdgovor.setText("Morate uneti racun sa kog skidate novac!");
                            return;
                        }
                        
                        if (tip.equals("prenos") && (transakcijaRacunSa.getText().length() == 0 || transakcijaRacunKa.getText().length() == 0)) {
                            transakcijaOdgovor.setText("Morate uneti racune!");
                            return;
                        }
                        
                        if (transakcijaIznos.getText().length() == 0) {
                            transakcijaOdgovor.setText("Morate uneti iznos!");
                            return;
                        }
                        
                        if (transakcijaSvrha.getText().length() == 0) {
                            transakcijaOdgovor.setText("Morate uneti svrhu!");
                            return;
                        }
                        
                        if (transakcijaFilijala.getText().length() == 0 && (tip.equals("uplata") || tip.equals("isplata")) ) {
                            transakcijaOdgovor.setText("Morate uneti filijalu za uplatu i isplatu!");
                            return;
                        }

                        queryParam.append("tip=");
                        queryParam.append(tip);
                        queryParam.append("&");
                        queryParam.append("racunsa=");
                        queryParam.append(transakcijaRacunSa.getText());
                        queryParam.append("&");
                        queryParam.append("racunka=");
                        queryParam.append(transakcijaRacunKa.getText());
                        queryParam.append("&");
                        queryParam.append("svrha=");
                        queryParam.append(transakcijaSvrha.getText());
                        queryParam.append("&");
                        queryParam.append("iznos=");
                        queryParam.append(transakcijaIznos.getText());
                        queryParam.append("&");
                        queryParam.append("filijala=");
                        queryParam.append(transakcijaFilijala.getText());

                        odgovor = transakcijaOdgovor;
                        
                    } break;
                    
                }
                
                con.setDoOutput(true);
            
                OutputStream output = con.getOutputStream();
                output.write(queryParam.toString().getBytes());
                output.flush();

                responseCode = con.getResponseCode();
                odgovor.append(Integer.toString(responseCode));

                BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()));
                while ((inputString = in.readLine()) != null) {
                    odgovor.append(inputString);
                }
                in.close(); 

            } else if (method.equals("GET")) {
                
                odgovor = rezultatDohvatanja;
                odgovor.setText("");
                
                responseCode = con.getResponseCode();
                odgovor.append(Integer.toString(responseCode));
                odgovor.append("\n");

                BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()));
                while ((inputString = in.readLine()) != null) {
                    StringTokenizer stringTokenizer = new StringTokenizer(inputString, "*");
                    int i = 0;
                    while (stringTokenizer.hasMoreTokens()) {
                        String next = stringTokenizer.nextToken();
                        if (next.charAt(0) == '[') next = next.substring(1);
                        if (i > 1 && next.length() > 3) next = next.substring(2);
                        if (next.length() > 3) {
                            odgovor.append(next);
                            odgovor.append("\n");
                        }
                        i++;
                    }
                }
                in.close();

            } else if (method.equals("PUT")) {
                //StringTokenizer tokenizer = new StringTokenizer(endpoint, "/");
                //endpoint = tokenizer.nextToken();
                if (endpoint.equals("sedista")) {
                        
                        if (promenaSedistaKomitent.getText().length() == 0 || promenaSedistaSediste.getText().length() == 0) {
                            odgovorPromenaSedista.setText("Polja nisu ispravno uneta!");
                            return;
                        } else odgovorPromenaSedista.setText("");

                        queryParam.append("komitent=");
                        queryParam.append(promenaSedistaKomitent.getText());
                        queryParam.append("&");
                        queryParam.append("sediste=");
                        queryParam.append(promenaSedistaSediste.getText());

                        odgovor = odgovorPromenaSedista;
                        
                         con.setDoOutput(true);
            
                        OutputStream output = con.getOutputStream();
                        output.write(queryParam.toString().getBytes());
                        output.flush();
                        
                }
                else {
                    System.out.println(endpoint.substring(0, 10));
                    if (endpoint.substring(0, 10).equals("zatvaranje")) {

                        odgovor = racunOdgovor;
                        odgovor.setText("");

                    }
                }
               

                responseCode = con.getResponseCode();
                odgovor.append(Integer.toString(responseCode));

                BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()));
                while ((inputString = in.readLine()) != null) {
                    odgovor.append(inputString);
                }
                in.close(); 
                
            }
            
            
                    
        } catch (MalformedURLException ex) {
            Logger.getLogger(Prozor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Prozor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

